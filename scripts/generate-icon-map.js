#!/usr/bin/env node
/**
 * Generate `packages/components/icon/src/svg-map.generated.ts` — a TypeScript
 * constant mapping every bundled Tabler icon name to its raw SVG string.
 *
 * The set of bundled icons is the union of `outlined` and `filled` arrays in
 * `packages/components/icon/icons.allowlist.json`. Filled variants get a
 * `-filled` suffix so `<kp-icon name="star-filled">` is unambiguous.
 *
 * Each SVG is minified (whitespace collapsed, redundant `class` attribute
 * stripped). Ship-time defaults stay around the size of the Figma 'Tabler
 * Icons' page; consumers extend at runtime via `KP_ICON_REGISTRY.register`.
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const TABLER_ROOT = path.join(ROOT, 'node_modules', '@tabler', 'icons', 'icons');
const ALLOWLIST = path.join(ROOT, 'packages', 'components', 'icon', 'icons.allowlist.json');
const OUT = path.join(ROOT, 'packages', 'components', 'icon', 'src', 'svg-map.generated.ts');

function minifySvg(svg) {
  return svg
    .replace(/\n\s*/g, ' ')
    .replace(/>\s+</g, '><')
    .replace(/\s+/g, ' ')
    .replace(/\s*=\s*/g, '=')
    .replace(/<svg[^>]*class=\"[^\"]*\"/, m => m.replace(/\s*class=\"[^\"]*\"/, ''))
    .trim();
}

function loadAllowlist() {
  const raw = JSON.parse(fs.readFileSync(ALLOWLIST, 'utf8'));
  return {
    outlined: (raw.outlined || []).slice().sort(),
    filled: (raw.filled || []).slice().sort(),
  };
}

function readIcon(variant, name) {
  const file = path.join(TABLER_ROOT, variant, name + '.svg');
  if (!fs.existsSync(file)) {
    throw new Error(`Tabler icon missing: ${variant}/${name}.svg (check icons.allowlist.json)`);
  }
  return minifySvg(fs.readFileSync(file, 'utf8'));
}

const list = loadAllowlist();
const outlined = list.outlined.map((name) => ({ name, svg: readIcon('outline', name) }));
const filled = list.filled.map((name) => ({ name, svg: readIcon('filled', name) }));

const lines = [];
lines.push('/* eslint-disable */');
lines.push('// Generated by scripts/generate-icon-map.js — DO NOT EDIT BY HAND.');
lines.push('// Source: @tabler/icons (MIT). Set is governed by icons.allowlist.json.');
lines.push('// Regenerate with `npm run generate:icons` (also runs during `build:libs`).');
lines.push('');
lines.push('export const TABLER_SVG_MAP: Readonly<Record<string, string>> = Object.freeze({');
for (const { name, svg } of outlined) {
  lines.push(`  ${JSON.stringify(name)}: ${JSON.stringify(svg)},`);
}
for (const { name, svg } of filled) {
  lines.push(`  ${JSON.stringify(name + '-filled')}: ${JSON.stringify(svg)},`);
}
lines.push('});');
lines.push('');
lines.push(`export const TABLER_OUTLINED_NAMES: readonly string[] = Object.freeze([${outlined.map((i) => JSON.stringify(i.name)).join(',')}]);`);
lines.push(`export const TABLER_FILLED_NAMES: readonly string[] = Object.freeze([${filled.map((i) => JSON.stringify(i.name + '-filled')).join(',')}]);`);
lines.push('');

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, lines.join('\n'));

const sizeKb = Math.round(fs.statSync(OUT).size / 1024);
console.log(`icon map: ${outlined.length} outlined + ${filled.length} filled → ${path.relative(ROOT, OUT)} (${sizeKb} KB)`);
