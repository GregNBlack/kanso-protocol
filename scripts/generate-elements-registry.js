#!/usr/bin/env node
/**
 * generate-elements-registry — scan packages/ui for every `kp-*` element
 * component and emit the custom-elements registry consumed by the
 * @angular/elements bundle (packages/elements).
 *
 * Output: packages/elements/src/registry.generated.ts
 *   import { KpBadgeComponent } from '@kanso-protocol/ui/badge';
 *   …
 *   export const KANSO_ELEMENTS = [{ tag: 'kp-badge', component: KpBadgeComponent }, …];
 *
 * Only element-selector components are included. Attribute-selector
 * (`button[kpButton]`, `button[kpTab]`) and structural directives
 * (`[kpVirtualRow]`, `ng-template[kpTableCell]`) can't be custom elements —
 * they're documented as Angular-template-only in docs/web-components.md.
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const UI = path.join(ROOT, 'packages', 'ui');
const OUT = path.join(ROOT, 'packages', 'elements', 'src', 'registry.generated.ts');
// Granular per-component entry points (Feature: tree-shakeable elements) are
// generated alongside the all-in-one registry, from the SAME scan — never a
// hand-maintained parallel list.
const DEFINE_DIR = path.join(ROOT, 'packages', 'elements', 'src', 'define');
const MANIFEST = path.join(DEFINE_DIR, 'manifest.json');
const SKIP = new Set(['src', 'styles', 'stories']);

// folder → Set of class names; tag list with their class + entry point
const byEntry = new Map();
const elements = []; // { tag, cls, entry }

for (const dir of fs.readdirSync(UI).sort()) {
  if (SKIP.has(dir)) continue;
  const srcDir = path.join(UI, dir, 'src');
  if (!fs.existsSync(srcDir)) continue;
  for (const f of fs.readdirSync(srcDir)) {
    if (!f.endsWith('.component.ts')) continue;
    const txt = fs.readFileSync(path.join(srcDir, f), 'utf8');
    const re = /@Component\(\{[\s\S]*?selector:\s*['"]([^'"]+)['"][\s\S]*?\}\)\s*export class (\w+)/g;
    let m;
    while ((m = re.exec(txt))) {
      const [, sel, cls] = m;
      if (!/^kp-[a-z0-9-]+$/.test(sel)) continue; // element selectors only
      if (!byEntry.has(dir)) byEntry.set(dir, new Set());
      byEntry.get(dir).add(cls);
      elements.push({ tag: sel, cls, entry: dir });
    }
  }
}

elements.sort((a, b) => a.tag.localeCompare(b.tag));

// tag → { subpath, fn } — subpath is the tag minus the `kp-` prefix (unique),
// fn is `defineKp` + PascalCase(subpath). Shared with build-elements.js via
// the manifest, so naming is derived once.
const pascal = (s) => s.split('-').map((p) => p.charAt(0).toUpperCase() + p.slice(1)).join('');
const granular = elements.map((e) => {
  const subpath = e.tag.replace(/^kp-/, '');
  return { tag: e.tag, cls: e.cls, entry: e.entry, subpath, fn: `defineKp${pascal(subpath)}` };
});

const imports = [...byEntry.entries()]
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([dir, classes]) => `import { ${[...classes].sort().join(', ')} } from '@kanso-protocol/ui/${dir}';`)
  .join('\n');

const registry = elements.map((e) => `  { tag: '${e.tag}', component: ${e.cls} },`).join('\n');

const out = `// AUTO-GENERATED by scripts/generate-elements-registry.js — do not edit.
// ${elements.length} element-selector components across ${byEntry.size} entry points.
import type { Type } from '@angular/core';
${imports}

export interface KansoElementDef {
  readonly tag: string;
  readonly component: Type<unknown>;
}

export const KANSO_ELEMENTS: readonly KansoElementDef[] = [
${registry}
];
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, out);
console.log(`elements registry: ${elements.length} custom elements across ${byEntry.size} entry points → ${path.relative(ROOT, OUT)}`);

// ─── Granular per-component entry points ─────────────────────────────────
// One `define/<subpath>.ts` per element, each importing only its own
// component + the shared runtime. build-elements.js code-splits these so a
// consumer can `import '@kanso-protocol/elements/badge'` (or call
// `defineKpBadge()`) and register just that element — the Angular runtime is
// shared, but no other component's code is pulled in.

// Reset the generated dir so a removed component leaves no orphan entry.
fs.rmSync(DEFINE_DIR, { recursive: true, force: true });
fs.mkdirSync(DEFINE_DIR, { recursive: true });

for (const g of granular) {
  const src = `// AUTO-GENERATED by scripts/generate-elements-registry.js — do not edit.
import { ${g.cls} } from '@kanso-protocol/ui/${g.entry}';
import { defineKansoElement } from '../runtime';

/** Register just the \`<${g.tag}>\` custom element. Idempotent. */
export function ${g.fn}(): Promise<void> {
  return defineKansoElement('${g.tag}', ${g.cls});
}

// Side-effect: importing this entry auto-registers the element, mirroring the
// all-in-one bundle (\`import '@kanso-protocol/elements/${g.subpath}'\`).
if (typeof customElements !== 'undefined') {
  void ${g.fn}();
}
`;
  fs.writeFileSync(path.join(DEFINE_DIR, `${g.subpath}.ts`), src);
}

// Machine-readable manifest consumed by build-elements.js to build the
// entry points, the published exports map, and the per-entry .d.ts files.
fs.writeFileSync(
  MANIFEST,
  JSON.stringify(granular.map(({ tag, subpath, fn }) => ({ tag, subpath, fn })), null, 2) + '\n',
);
console.log(`elements granular entries: ${granular.length} → ${path.relative(ROOT, DEFINE_DIR)}/*.ts`);
