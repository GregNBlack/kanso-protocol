#!/usr/bin/env node
/**
 * generate-elements-registry — scan packages/ui for every `kp-*` element
 * component and emit the custom-elements registry consumed by the
 * @angular/elements bundle (packages/elements).
 *
 * Output: packages/elements/src/registry.generated.ts
 *   import { KpBadgeComponent } from '@kanso-protocol/ui/badge';
 *   …
 *   export const KANSO_ELEMENTS = [{ tag: 'kp-badge', component: KpBadgeComponent }, …];
 *
 * Only element-selector components are included. Attribute-selector
 * (`button[kpButton]`, `button[kpTab]`) and structural directives
 * (`[kpVirtualRow]`, `ng-template[kpTableCell]`) can't be custom elements —
 * they're documented as Angular-template-only in docs/web-components.md.
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const UI = path.join(ROOT, 'packages', 'ui');
const OUT = path.join(ROOT, 'packages', 'elements', 'src', 'registry.generated.ts');
const SKIP = new Set(['src', 'styles', 'stories']);

// folder → Set of class names; tag list with their class
const byEntry = new Map();
const elements = []; // { tag, cls }

for (const dir of fs.readdirSync(UI).sort()) {
  if (SKIP.has(dir)) continue;
  const srcDir = path.join(UI, dir, 'src');
  if (!fs.existsSync(srcDir)) continue;
  for (const f of fs.readdirSync(srcDir)) {
    if (!f.endsWith('.component.ts')) continue;
    const txt = fs.readFileSync(path.join(srcDir, f), 'utf8');
    const re = /@Component\(\{[\s\S]*?selector:\s*['"]([^'"]+)['"][\s\S]*?\}\)\s*export class (\w+)/g;
    let m;
    while ((m = re.exec(txt))) {
      const [, sel, cls] = m;
      if (!/^kp-[a-z0-9-]+$/.test(sel)) continue; // element selectors only
      if (!byEntry.has(dir)) byEntry.set(dir, new Set());
      byEntry.get(dir).add(cls);
      elements.push({ tag: sel, cls });
    }
  }
}

elements.sort((a, b) => a.tag.localeCompare(b.tag));

const imports = [...byEntry.entries()]
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([dir, classes]) => `import { ${[...classes].sort().join(', ')} } from '@kanso-protocol/ui/${dir}';`)
  .join('\n');

const registry = elements.map((e) => `  { tag: '${e.tag}', component: ${e.cls} },`).join('\n');

const out = `// AUTO-GENERATED by scripts/generate-elements-registry.js — do not edit.
// ${elements.length} element-selector components across ${byEntry.size} entry points.
import type { Type } from '@angular/core';
${imports}

export interface KansoElementDef {
  readonly tag: string;
  readonly component: Type<unknown>;
}

export const KANSO_ELEMENTS: readonly KansoElementDef[] = [
${registry}
];
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, out);
console.log(`elements registry: ${elements.length} custom elements across ${byEntry.size} entry points → ${path.relative(ROOT, OUT)}`);
