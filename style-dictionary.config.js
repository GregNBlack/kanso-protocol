//
// Each theme is `tokens/themes/<name>.json` → `packages/ui/styles/<name>.css`
// under `[data-theme="<name>"]`. They can't just be extra platforms on the
// default config: Style Dictionary would try to resolve every primitive /
// semantic token AND emit them into the theme file, but a theme file must
// contain *only* its overrides — so each theme gets its own config with a
// single-file source list. `scripts/build-tokens.js` loops over `themeConfigs`.
//
// To add a theme: drop `tokens/themes/<name>.json`, add an entry to THEMES
// with its prelude (and an optional header note), and rebuild.
