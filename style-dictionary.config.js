function makeThemeConfig(theme) {
  return {
    hooks,
    source: [`tokens/themes/${theme.name}.json`],
    platforms: {
      css: {
        transformGroup: 'css',
        prefix: 'kp',
        buildPath: 'packages/ui/styles/',
        files: [
          {
            destination: `${theme.name}.css`,
            format: 'css/variables-theme',
            // Keep the var() chain for semantic overrides that reference a
            // primitive stop. This makes each theme brand-aware: overriding a
            // --kp-color-<ramp>-* at runtime cascades to these tokens, exactly
            // like the light theme. Primitive ramp stops have no references,
            // so they stay literal.
            options: {
              outputReferences: true,
              themeName: theme.name,
              prelude: theme.prelude,
              note: theme.note,
            },
          },
        ],
      },
    },
  };
}

module.exports.themes = THEMES;
module.exports.makeThemeConfig = makeThemeConfig;
module.exports.themeConfigs = THEMES.map(makeThemeConfig);
